	/**
	 * 查找名字相等的lnf
	 * 
	 * @param name
	 * @return 默认返回WindowsLookAndFeel
	 */
	public static LookAndFeelInfo searchLnf(String name) {
		if (name == null || name.length() == 0) {
			return LNF_DEFAULT;
		}
		for (LookAndFeelInfo lnf : LNFS) {
			if (lnf.getName().equals(name)) {
				return lnf;
			}
		}
		return LNF_DEFAULT;
	}
